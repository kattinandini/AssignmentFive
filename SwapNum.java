package com.Bridgelab.assignmentfive;

public class SwapNum {
    public static void main(String[] args) {
        int a = 4;
        int b=6;
        System.out.println("before swaping........ a = " + a + " b = "+ b);
        int temp=a;

        a=b;
        b=temp;
        System.out.println("After swaping........ a = " + a + " b = "+ b);

    }
}
